BEGIN TRANSACTION;
CREATE TABLE WILAYA_FR(
    name_fr TEXT PRIMARY KEY
);
CREATE TABLE WILAYA_CODE_TEL(
    code_tel TEXT PRIMARY KEY
);
CREATE TABLE WILAYA_CODE_POST(
    code_post TEXT PRIMARY KEY
);
CREATE TABLE WILAYA_CODE(
    code TEXT PRIMARY KEY
);
CREATE TABLE WILAYA_AR(
    name_ar TEXT PRIMARY KEY
);
CREATE TABLE WILAYA(
    code TEXT PRIMARY KEY,
    code_tel TEXT NOT NULL,
    code_post TEXT NOT NULL,
    name_ar TEXT NOT NULL,
    name_fr TEXT NOT NULL,
    name_en TEXT NOT NULL
);
CREATE TABLE BALADIYA_FR(
    name_fr TEXT PRIMARY KEY
);
CREATE TABLE BALADIYA_CODE(
		    code TEXT PRIMARY KEY
		);
CREATE TABLE BALADIYA_AR(
    name_ar TEXT PRIMARY KEY
);
CREATE TABLE BALADIYA(
    code TEXT PRIMARY KEY,
    name_ar TEXT NOT NULL,
    name_fr TEXT NOT NULL,
    name_en TEXT NOT NULL,
    wilaya_code TEXT NOT NULL,
    FOREIGN KEY(wilaya_code) REFERENCES WILAYA(code)
);
